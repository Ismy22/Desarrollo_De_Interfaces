    public void ReproducirSonido(boolean seguir) {
        try {
            AudioInputStream audioInputStream = null;

            Clip clip = null;
            String nombreSonido = "C:\\Users\\Ismael\\Documents\\NetBeansProjects\\BBDD_Zoo_Interfaz\\src\\Login\\Imagen\\jPark.wav";
            audioInputStream = AudioSystem.getAudioInputStream(new File(nombreSonido).getAbsoluteFile());
            clip = AudioSystem.getClip();
            clip.open(audioInputStream);
            if (seguir) {
                clip.start();
            } else {
                clip.stop();
            }
        } catch (UnsupportedAudioFileException ex) {
            Logger.getLogger(LoginUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(LoginUser.class.getName()).log(Level.SEVERE, null, ex);
        } catch (LineUnavailableException ex) {
            Logger.getLogger(LoginUser.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

