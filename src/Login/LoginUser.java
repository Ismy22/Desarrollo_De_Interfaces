    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        //COMPROBAMOS QUE ES CORRECTO
        String dni = jTUserLogin.getText();
        char[] arrayC = jPasswordField1.getPassword();
        String pass = new String(arrayC);
        String passMd5 = Utilidades.Utilidades_Control.getMD5(pass);
        System.out.println(passMd5);

        if (dni.equals("") || pass.equals("")) {

            JOptionPane.showMessageDialog(null, "no puede dejar ning√∫n espacio en blanco");
        } else {

            if (DBManagerZoo.getLogin(dni, passMd5) == null) {
                JOptionPane.showMessageDialog(null, "No hay ning√∫n usuario con esa contrase√±a, int√©ntelo de nuevo o registrese");

            } else {

                String cargo = DBManagerZoo.getCargoLogin(dni, passMd5);

                switch (cargo) {
                    case "Empleado":

                        bbdd_Zoo_Interfaz.Panel_Empleados open = new bbdd_Zoo_Interfaz.Panel_Empleados(dni);
                        open.setVisible(true);
                        this.setVisible(false);

                        break;

                    case "Administrador":
                        boolean seguir = false;
                        ReproducirSonido(seguir);
                        bbdd_Zoo_Interfaz.Panel_Admin openAdmin = new bbdd_Zoo_Interfaz.Panel_Admin(dni);
                        openAdmin.setVisible(true);

                        this.dispose();

                        break;

                    case "Visitante":

                        bbdd_Zoo_Interfaz.Panel_Visitante openVisitante = new bbdd_Zoo_Interfaz.Panel_Visitante(dni);
                        openVisitante.setVisible(true);
                        openVisitante.rellenarTablaVisitantes();
                        this.setVisible(false);

                        break;

                    default:
                        throw new AssertionError();
                }

            }
        }

    }//GEN-LAST:event_jButtonLoginActionPerformed

